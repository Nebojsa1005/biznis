    @Test
    @Transactional
    void putExistingJob() throws Exception {
        // Initialize the database
        jobRepository.saveAndFlush(job);

        int databaseSizeBeforeUpdate = jobRepository.findAll().size();

        // Update the job
        Job updatedJob = jobRepository.findById(job.getId()).get();
        // Disconnect from session so that the updates on updatedJob are not directly saved in db
        em.detach(updatedJob);
        updatedJob
            .title(UPDATED_TITLE)
            .description(UPDATED_DESCRIPTION)
            .dificulty(UPDATED_DIFICULTY)
            .dateFrom(UPDATED_DATE_FROM)
            .dateTo(UPDATED_DATE_TO)
            .isActive(UPDATED_IS_ACTIVE)
            .jobType(UPDATED_JOB_TYPE)
            .typeOfJob(UPDATED_TYPE_OF_JOB);

        restJobMockMvc
            .perform(
                put(ENTITY_API_URL_ID, updatedJob.getId())
                    .contentType(MediaType.APPLICATION_JSON)
                    .content(TestUtil.convertObjectToJsonBytes(updatedJob))
            )
            .andExpect(status().isOk());

        // Validate the Job in the database
        List<Job> jobList = jobRepository.findAll();
        assertThat(jobList).hasSize(databaseSizeBeforeUpdate);
        Job testJob = jobList.get(jobList.size() - 1);
        assertThat(testJob.getTitle()).isEqualTo(UPDATED_TITLE);
        assertThat(testJob.getDescription()).isEqualTo(UPDATED_DESCRIPTION);
        assertThat(testJob.getDificulty()).isEqualTo(UPDATED_DIFICULTY);
        assertThat(testJob.getDateFrom()).isEqualTo(UPDATED_DATE_FROM);
        assertThat(testJob.getDateTo()).isEqualTo(UPDATED_DATE_TO);
        assertThat(testJob.getIsActive()).isEqualTo(UPDATED_IS_ACTIVE);
        assertThat(testJob.getJobType()).isEqualTo(UPDATED_JOB_TYPE);
        assertThat(testJob.getTypeOfJob()).isEqualTo(UPDATED_TYPE_OF_JOB);
    }

