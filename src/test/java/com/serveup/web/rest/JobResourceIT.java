    @Test
    @Transactional
    void createJob() throws Exception {
        int databaseSizeBeforeCreate = jobRepository.findAll().size();
        // Create the Job
        restJobMockMvc
            .perform(post(ENTITY_API_URL).contentType(MediaType.APPLICATION_JSON).content(TestUtil.convertObjectToJsonBytes(job)))
            .andExpect(status().isCreated());

        // Validate the Job in the database
        List<Job> jobList = jobRepository.findAll();
        assertThat(jobList).hasSize(databaseSizeBeforeCreate + 1);
        Job testJob = jobList.get(jobList.size() - 1);
        assertThat(testJob.getTitle()).isEqualTo(DEFAULT_TITLE);
        assertThat(testJob.getDescription()).isEqualTo(DEFAULT_DESCRIPTION);
        assertThat(testJob.getDificulty()).isEqualTo(DEFAULT_DIFICULTY);
        assertThat(testJob.getDateFrom()).isEqualTo(DEFAULT_DATE_FROM);
        assertThat(testJob.getDateTo()).isEqualTo(DEFAULT_DATE_TO);
        assertThat(testJob.getIsActive()).isEqualTo(DEFAULT_IS_ACTIVE);
        assertThat(testJob.getJobType()).isEqualTo(DEFAULT_JOB_TYPE);
        assertThat(testJob.getTypeOfJob()).isEqualTo(DEFAULT_TYPE_OF_JOB);
    }

